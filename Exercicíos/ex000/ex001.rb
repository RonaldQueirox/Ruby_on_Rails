def soma a, b  # o def serve para a definição de um método
    a + b # codigo que esta sendo feito, bem semelhante a python
end # fim da função

puts soma 1, 2 # puts serve como o print ou echo

# outra forma, com parenteses

def somar (d,e) # ponto e virgula são opcionais
    d + e
end

puts somar (4, 6)